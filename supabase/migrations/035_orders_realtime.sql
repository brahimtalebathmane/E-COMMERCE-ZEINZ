-- Enable Supabase Realtime on orders for live admin dashboard sync.
-- RLS (orders_select_admin) scopes which rows authenticated admins receive.

do $$
begin
  if not exists (
    select 1
    from pg_publication_tables
    where pubname = 'supabase_realtime'
      and schemaname = 'public'
      and tablename = 'orders'
  ) then
    alter publication supabase_realtime add table public.orders;
  end if;
end $$;
